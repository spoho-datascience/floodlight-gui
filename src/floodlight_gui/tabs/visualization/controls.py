def create_visualization_tab():
    """Build the Visualization tab: pitch canvas, playback controls, overlays, export.

    Creates all DPG widgets for the tab, registers mouse handlers, and subscribes
    to EventBus events. Must be called once during app startup inside an active
    DPG context.

    Notes
    -----
    DPG tags owned by this function (directly or via helpers called here):

    - ``viz_tab`` -- the DPG tab item
    - ``viz_content_wrap`` -- full-height child window wrapping all content
    - ``_DRAWLIST_CONTAINER_TAG``, ``_DRAWLIST_TAG`` -- pitch canvas container and drawlist
    - ``_TIMELINE_CONTAINER_TAG``, ``_TIMELINE_TAG``, ``_TIMELINE_HOVER_TAG`` -- Code-object
      timeline strip (hidden until Code data is available)
    - All tags created by ``_build_control_panel`` (period combo, team checkboxes,
      frame slider, transport buttons, overlay toggles, export panels)

    EventBus subscriptions wired here:

    - ``Events.DATA_LOADED`` -> ``_on_data_loaded`` (render_loop), priority 10
    - ``Events.DATA_LOADED`` -> ``_on_data_loaded_timeline`` (timeline), priority 10
    - ``Events.FRAME_CHANGED`` -> ``_on_frame_changed_timeline`` (timeline), priority 10
    - ``Events.MODEL_FITTED`` -> ``_on_model_fitted`` (overlay_dispatch), priority 10
    - ``Events.XY_STACK_CHANGED`` -> ``_on_xy_stack_changed`` (render_loop), priority 10

    Side-effects:

    - Calls ``_register_mouse_handlers()`` to attach a global DPG mouse handler.
    - Calls ``playback._init_fps_overlay()`` (lazy import to break the
      controls->playback->controls circular import).
    """
    with dpg.tab(label="Visualization", tag="viz_tab"):
        # Tab header mounts the unified data-context summary shared with all tabs.
        add_tab_header("visualization")

        with dpg.child_window(
            tag="viz_content_wrap",
            height=-FOOTER_HEIGHT_PX,
            border=False,
            autosize_x=True,
        ):
            dpg.add_text("Interactive Match Visualization", color=INFO)
            dpg.add_separator()

            with dpg.group(horizontal=True):
                # ----- Left panel: controls (fixed 350 px) -----
                with dpg.child_window(width=_CONTROL_PANEL_WIDTH, height=-1):
                    _build_control_panel()

                # ----- Right panel: drawlist + Code-object timeline strip -----
                with dpg.child_window(
                    width=-1, height=-1, tag=_DRAWLIST_CONTAINER_TAG, border=False
                ):
                    dpg.add_text(
                        "Load match data to begin",
                        tag="viz_plot_placeholder",
                    )
                    dpg.add_drawlist(
                        width=_DRAWLIST_INITIAL_WIDTH,
                        height=_DRAWLIST_INITIAL_HEIGHT,
                        tag=_DRAWLIST_TAG,
                        show=False,
                    )

                    # Code-object timeline strip (below pitch, above playback toolbar).
                    with dpg.child_window(
                        tag=_TIMELINE_CONTAINER_TAG,
                        width=_TIMELINE_WIDTH_INITIAL + 4,
                        height=_TIMELINE_ROW_HEIGHT + 6,
                        show=False,  # hidden until codes available
                        border=False,
                    ):
                        dpg.add_drawlist(
                            tag=_TIMELINE_TAG,
                            width=_TIMELINE_WIDTH_INITIAL,
                            height=_TIMELINE_ROW_HEIGHT,
                        )
                        dpg.add_text(
                            "",
                            tag=_TIMELINE_HOVER_TAG,
                            color=INFO,
                            show=False,
                        )

    # Register a global mouse handler for hover / click on the drawlist.
    _register_mouse_handlers()

    # Subscribe to event-bus signals (single wiring point for the subpackage).
    bus.subscribe(Events.DATA_LOADED, _on_data_loaded, priority=10)
    bus.subscribe(Events.DATA_LOADED, _on_data_loaded_timeline, priority=10)
    bus.subscribe(Events.FRAME_CHANGED, _on_frame_changed_timeline, priority=10)
    bus.subscribe(Events.MODEL_FITTED, _on_model_fitted, priority=10)
    bus.subscribe(Events.XY_STACK_CHANGED, _on_xy_stack_changed, priority=10)

    # Lazy import to avoid controls->playback->controls circular import.
    from floodlight_gui.tabs.visualization import playback as _pb

    _pb._init_fps_overlay()


