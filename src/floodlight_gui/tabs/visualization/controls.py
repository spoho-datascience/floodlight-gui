def _on_period_changed(sender, app_data):
    """Handle period combo selection change."""
    _stop_playback_if_playing("Period changed")

    temporal_divisions = state.app_instance.get_temporal_divisions() if state.app_instance else []
    half_name = period_display_to_internal(app_data) if app_data else None
    if half_name is None or (half_name not in temporal_divisions and temporal_divisions):
        items = dpg.get_item_configuration("viz_period_selection").get("items", [])
        try:
            idx = items.index(app_data)
            half_name = temporal_divisions[idx]
        except (ValueError, IndexError):
            half_name = temporal_divisions[0] if temporal_divisions else "firstHalf"

    state.viz_state["selected_half"] = half_name

    if state.viz_state["initialized"]:
        initialize_visualization()


def _on_team_checkbox(sender, app_data):
    """Handle team visibility checkbox toggle."""
    from floodlight_gui.tabs.visualization.render_loop import _render_current_frame

    team_names = state.viz_state["cached_team_names"]
    if team_names is None:
        return

    renderer = state.viz_state["player_renderer"]

    for team in team_names:
        tag = f"viz_show_{team.lower().replace(' ', '_')}"
        if dpg.does_item_exist(tag):
            visible = dpg.get_value(tag)
            state.viz_state["selected_teams"][team] = visible
            if renderer is not None:
                try:
                    renderer.set_team_visible(team, visible)
                except SystemError as e:
                    logger.warning("Error toggling team %s: %s", team, e)

    _render_current_frame()


def _on_label_field_changed(sender, app_data):
    """Operator chose a teamsheet column (or 'Off') for on-canvas player labels."""
    renderer = state.viz_state.get("player_renderer")
    if renderer is None:
        return
    try:
        from floodlight_gui.tabs.visualization.render_loop import _render_current_frame

        if not app_data or app_data == _LABEL_FIELD_OFF:
            renderer.set_labels_visible(False)
            renderer.set_label_resolver(None)
            _render_current_frame()  # reflect immediately, not on the next tick
            return

        from floodlight_gui.core.player_mapping import teamsheet_column_values

        column = app_data
        teamsheet = getattr(state.app_instance, "teamsheet", None) if state.app_instance else None
        teams = state.viz_state.get("cached_team_names") or []
        # Precompute {team: [value per col_index]} once so the per-frame resolver
        # is a cheap list index, never a DataFrame read.
        per_team = {team: teamsheet_column_values(teamsheet, team, column) for team in teams}

        def _resolver(team: str, idx: int, _col=column, _map=per_team) -> str | None:
            values = _map.get(team) or []
            if 0 <= idx < len(values) and values[idx] is not None:
                return values[idx]
            # xID is the x-coordinate column index -- always derivable from the
            # array position even when the teamsheet doesn't carry it.
            if _col == _LABEL_FIELD_XID:
                return str(idx)
            return None

        renderer.set_label_resolver(_resolver)
        renderer.set_labels_visible(True)
        _render_current_frame()  # reflect immediately, not on the next tick
    except SystemError as exc:
        logger.warning("Error applying player label field (DPG item missing): %s", exc)


def _populate_label_field_combo() -> None:
    """Refresh the player-label combo from the loaded teamsheets' column labels."""
    if state.app_instance is None or not getattr(state.app_instance, "loaded_data", None):
        return
    if not dpg.does_item_exist("viz_label_field"):
        return

    from floodlight_gui.core.player_mapping import teamsheet_columns_for

    teams = state.viz_state.get("cached_team_names") or []
    teamsheet = getattr(state.app_instance, "teamsheet", None)
    if not teams and isinstance(teamsheet, dict):
        teams = list(teamsheet.keys())

    # Verbatim teamsheet column labels (none when no teamsheet is loaded), plus
    # the always-available xID. No fabricated / synthesized fields.
    columns = teamsheet_columns_for(teamsheet, teams)
    fields = [_LABEL_FIELD_XID] + [c for c in columns if c != _LABEL_FIELD_XID]
    items = [_LABEL_FIELD_OFF] + fields
    try:
        current = dpg.get_value("viz_label_field") or _LABEL_FIELD_OFF
        dpg.configure_item("viz_label_field", items=items)
        if current not in items:
            dpg.set_value("viz_label_field", _LABEL_FIELD_OFF)
            _on_label_field_changed(None, _LABEL_FIELD_OFF)
    except SystemError as exc:
        logger.debug("populate label combo failed: %s", exc)
