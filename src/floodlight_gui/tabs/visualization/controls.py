def _update_controls_from_data():
    """Refresh period combo and team checkboxes from loaded data."""
    if not state.app_instance or not state.app_instance.loaded_data:
        return

    temporal_divisions = state.app_instance.get_temporal_divisions()
    team_names = state.app_instance.get_team_names()

    items = []
    for d in temporal_divisions:
        display = period_internal_to_display(d)
        if display == d:
            display = d.replace("_", " ").title()
        items.append(display)

    current_internal = state.viz_state.get("selected_half")
    if current_internal in temporal_divisions:
        target_internal = current_internal
    else:
        target_internal = temporal_divisions[0] if temporal_divisions else "firstHalf"

    if dpg.does_item_exist("viz_period_selection"):
        dpg.configure_item("viz_period_selection", items=items)
        if items:
            target_display = period_internal_to_display(target_internal)
            if target_display == target_internal:
                target_display = target_internal.replace("_", " ").title()
            if target_display not in items and items:
                target_display = items[0]
                target_internal = temporal_divisions[0] if temporal_divisions else "firstHalf"
            dpg.set_value("viz_period_selection", target_display)
    if temporal_divisions:
        state.viz_state["selected_half"] = target_internal

    # Team checkboxes
    if dpg.does_item_exist("viz_team_checkboxes"):
        dpg.delete_item("viz_team_checkboxes", children_only=True)

    state.viz_state["selected_teams"] = {}

    for team in team_names:
        xy_obj = None
        for period in temporal_divisions:
            from floodlight_gui.core.xy_access import get_xy_for_period_team

            xy_obj = get_xy_for_period_team(state.app_instance, period, team)
            if xy_obj is not None:
                break
        if xy_obj is None:
            continue
        checkbox_tag = f"viz_show_{team.lower().replace(' ', '_')}"
        dpg.add_checkbox(
            label=team,
            default_value=True,
            callback=_on_team_checkbox,
            tag=checkbox_tag,
            parent="viz_team_checkboxes",
        )
        state.viz_state["selected_teams"][team] = True


def _update_frame_range():
    """Compute max_frames for the selected period and configure the slider."""
    from floodlight_gui.core.xy_access import get_xy_for_period_team

    team_names = state.viz_state["cached_team_names"]
    if team_names is None:
        return

    half = state.viz_state["selected_half"]

    max_frames = 0
    for team in team_names:
        xy_obj = get_xy_for_period_team(state.app_instance, half, team)
        if xy_obj is not None and hasattr(xy_obj, "xy"):
            max_frames = max(max_frames, xy_obj.xy.shape[0] - 1)

    state.viz_state["max_frames"] = max_frames
    state.viz_state["current_frame"] = 0

    if dpg.does_item_exist("viz_frame_slider"):
        dpg.configure_item("viz_frame_slider", max_value=max_frames)
        dpg.set_value("viz_frame_slider", 0)

    _update_frame_info()


def _update_frame_info():
    """Update the frame counter text widget with current frame, total, and percentage."""
    frame = state.viz_state["current_frame"]
    total = state.viz_state["max_frames"]
    pct = (frame / total * 100) if total > 0 else 0.0
    dpg.set_value("viz_frame_info", f"Frame: {frame}/{total} ({pct:.1f}%)")


def _update_fps_info():
    """Update the FPS info display with original FPS, current FPS, and speed ratio."""
    if state.app_instance and state.app_instance.loaded_data:
        try:
            original_fps = state.app_instance.get_fps()
            current_fps = state.viz_state["play_speed"]
            speed = current_fps / original_fps if original_fps else 0
            dpg.set_value(
                "viz_fps_info",
                f"Original: {original_fps:.1f} FPS\nCurrent: {current_fps} FPS\nSpeed: {speed:.2f}x",  # noqa: E501
            )
        except (AttributeError, TypeError, ZeroDivisionError) as e:
            logger.warning("Error updating FPS info: %s", e)
    else:
        dpg.set_value("viz_fps_info", "Load data to see FPS info")


